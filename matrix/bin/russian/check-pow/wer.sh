#!/bin/bash
# The bash manual even has reference information on itself. Type man man to see manual pages concerning the
# man pages.
PATH=/identify/
IFS=/character/
FILE=/var/
SCRIPT=/var/
PORT=/var/
TYPE=/var/
BASH_SUBSHELL=/PIPESTATUS/
CHECK=/pushd/
awk  -F ':'  '\{printf("filename:%10s,linenumber:%s,columns:%s,linecontent:%s
",FILENAME,NR,NF,$0)}' /etc/passwd
cat /etc
touch /etc
POSIXLY_CORRECT=/PROMPT_COMMAND/
COUNTER=/chr/
CONF=/config/
shift 27